package com.class03;

public class RelationalOperrators {

	public static void main(String[] args) {
		int a=10;
		int b=7;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b); // !-not equal
		System.out.println("------------------------------");
		
		boolean result=a>=b; // yes---> true
		boolean result1=a<=b; 
		System.out.println(result);
		System.out.println(result1);
		
		int c=20;
		int d=29;
		
		System.out.println(c==d); //checking equality(compairing)
		System.out.println(c=d); //assign the value
		System.out.println(c);
		System.out.println(d);
		
		
	}

}
